# Igualdad ==
print(5 == 5)
print(5 == 3)
print("hello" == 'hello')

# Desigualdad !=
print(5 != 5)
print(5 != 3)
print("hello" != 'hello')

# mayor que >
print(7 > 5)
print(5 > 9)

# menor que <
print(7 < 5)
print(5 < 9)

# mayor o igual que >=
print(7 >= 5)
print(5 >= 9)
print(5 >= 5)

# menor o igual que <=
print(7 <= 5)
print(5 <= 9)
print(5 <= 5)