value = 100
type_value = type(value)
print(value)
print(type_value)

#Int to Str
value = str(100) + 'Hola'
type_value = type(value)
print(value)
print(type_value)

#Str to Int
value = int('100')
type_value = type(value)
print(value)
print(type_value)

#Str to Float
value = float('100')
type_value = type(value)
print(value)
print(type_value)